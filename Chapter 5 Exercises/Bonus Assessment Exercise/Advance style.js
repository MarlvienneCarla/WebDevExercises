var samplesPerPage = 9;
var samples = document.querySelectorAll('.audio-sample');
var numSamples = samples.length;
var numPages = Math.ceil(numSamples / samplesPerPage);
var currentPage = 1;

// Hide previous button on first page
if (currentPage === 1) {
  document.querySelector('#prev-btn').style.display = 'none';
}

// Hide next button on last page
if (currentPage === numPages) {
  document.querySelector('#next-btn').style.display = 'none';
}

// Event listener for previous button
document.querySelector('#prev-btn').addEventListener('click', function() {
  if (currentPage > 1) {
    currentPage--;
    updateSamples();
  }
});

// Event listener for next button
document.querySelector('#next-btn').addEventListener('click', function() {
  if (currentPage < numPages) {
    currentPage++;
    updateSamples();
  }
});

// Function to update visible samples
function updateSamples() {
  var startIndex = (currentPage - 1) * samplesPerPage;
  var endIndex = startIndex + samplesPerPage;
  for (var i = 0; i < numSamples; i++) {
    if (i >= startIndex && i < endIndex) {
      samples[i].style.display = 'inline-block';
    } else {
      samples[i].style.display = 'none';
    }
  }
  
  // Show/hide previous and next buttons as appropriate
  if (currentPage === 1) {
    document.querySelector('#prev-btn').style.display = 'none';
  } else {
    document.querySelector('#prev-btn').style.display = 'inline-block';
  }
  
  if (currentPage === numPages) {
    document.querySelector('#next-btn').style.display = 'none';
  } else {
    document.querySelector('#next-btn').style.display = 'inline-block';
  }
}

// Initial update of visible samples
updateSamples();


